#include <stdio.h>

int main(int argc, char *argv[])
{
    char c = 'a';
    short n = 1;
    int m = 2;
    float x = 3.3;
    double y = 5.6;

    printf("c[%c]\n", c);
    printf("n[%d]\n", n);
    printf("m[%d]\n", m);
    printf("x[%f]\n", x);
    printf("y[%f]\n", y);

    return 0;
}
